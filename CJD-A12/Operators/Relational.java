class Relational{
	public static void main(String[] args){
		int a=15, b=10;
		System.out.println(a>b);
		System.out.println(a>=b);
		System.out.println(a<b);
		System.out.println(a<=b);
		System.out.println(a!=b);
		System.out.println(a==b);

		int a1=20, b1=20;
		System.out.println("\n"+(a1>b1));
		System.out.println(a1>=b1);
		System.out.println(a1<b1);
		System.out.println(a1<=b1);
		System.out.println(a1!=b1);
		System.out.println(a1==b1);
	}
}